package com.java.mahbixver20;

public class PopularCoffeeData {

    String name;
    String prize;

    public PopularCoffeeData(String name, String prize) {
        this.name = name;
        this.prize = prize;

    }


    public String getName() {
        return name;
    }
    public String getSize() {
        return prize;
    }
}

