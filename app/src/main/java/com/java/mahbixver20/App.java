package com.java.mahbixver20;

public class App {
    int image;
    String name;
    float prize;

    public App(int image, String name, float prize) {
        this.image = image;
        this.name = name;
        this.prize = prize;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public float getSize() {
        return prize;
    }
}
